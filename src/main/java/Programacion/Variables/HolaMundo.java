package Programacion.Variables;

public class HolaMundo {
    public static void main(String[] args) {
        String saludar="Hola mundo";
        String despedida = "Adios";
        System.out.println("saludar.toUpperCase() = " + saludar.toUpperCase());
        System.out.println("saludar.toLowerCase() = " + saludar.toLowerCase());
        System.out.println("despedida.toLowerCase() = " + despedida.toLowerCase());
        System.out.println("despedida = " + despedida.toUpperCase());

    }
}
